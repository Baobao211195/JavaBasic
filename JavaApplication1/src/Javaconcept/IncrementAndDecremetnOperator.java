public class IncrementAndDecremetnOperator {
    public static void main(String[] args) {
        int i = 0;
        System.out.println(i++); // output = 0
        System.out.println(i); // storage value = 1
        System.out.println( );
        
        int a= 0;
        System.out.println(++a);
        System.out.println(a);
        System.out.println( );
        
        int b= 0;
        System.out.println(b--); // output = 0
        System.out.println(b); // storage value = -1
        System.out.println( );
        
        int c= 0;
        System.out.println(--c); //output = -1
        System.out.println(c);
    }
            
    
}
